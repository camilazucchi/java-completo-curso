package membrosEstaticos.util;

public class Calculadora {

    public static double realizaSoma(double a, double b) {
        return a + b;
    }

    public static double realizaSubtracao(double a, double b) {
        return a - b;
    }

    public static double realizaMultiplicacao(double a, double b) {
        return a * b;
    }

    public static double realizaDivisao(double a, double b) {
        return a / b;
    }

}
