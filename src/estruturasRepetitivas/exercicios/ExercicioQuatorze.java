package estruturasRepetitivas.exercicios;

public class ExercicioQuatorze {
    public static void main(String[] args) {
        /* 14. Crie um programa que utilize um loop "for" para fazer uma contagem regressiva de 10 até 1.
        * Em cada iteração, o programa deve imprimir o número atual da contagem. */
        System.out.println("Contagem regressiva");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
        System.out.println("Contagem encerrada!");
    }
}
